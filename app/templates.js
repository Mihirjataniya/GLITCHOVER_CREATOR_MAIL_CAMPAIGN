// Helper function to get a random template
export const getRandomTemplate = (streamerName, platform) => {
  const randomIndex = Math.floor(Math.random() * emailTemplates.length);
  const template = emailTemplates[randomIndex];

  return {
    subject: template.subject.replace(/{{streamerName}}/g, streamerName),
    message: template.message
      .replace(/{{streamerName}}/g, streamerName)
      .replace(/{{platform}}/g, platform)
  };
};